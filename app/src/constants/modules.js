// Rebar weight lookup (lbs per lineal foot by bar size)
export const REBAR_WEIGHTS = {
  "#3": 0.376, "#4": 0.668, "#5": 1.043, "#6": 1.502, "#7": 2.044, "#8": 2.670,
};

// Helper: parse rebar spec like "#5 @ 12\" OC" → { bar: "#5", spacing: 12, lbsPerFt: 1.043 }
export function parseRebarSpec(spec) {
  if (!spec) return { bar: "#5", spacing: 12, lbsPerFt: 1.043 };
  const barMatch = spec.match(/#(\d)/);
  const spacingMatch = spec.match(/(\d+)"/);
  const bar = barMatch ? `#${barMatch[1]}` : "#5";
  const spacing = spacingMatch ? parseInt(spacingMatch[1]) : 12;
  const lbsPerFt = REBAR_WEIGHTS[bar] || 1.043;
  return { bar, spacing, lbsPerFt };
}

